let a = 20;
let b = 7;
// Operadores Aritméticos.
console.log('Adição (a + b):', a + b);         // Saída: 27
console.log('Subtração (a - b):', a - b);      // Saída: 13
console.log('Multiplicação (a * b):', a * b); // Saída: 140
console.log('Divisão (a / b):', a / b);       // Saída: 2.857142857142857
console.log('Módulo (a % b):', a % b);         // Saída: 6 (20 dividido por 7 é 2 com resto 6)
console.log('Exponenciação (a ** 2):', a ** 2); // Saída: 400 (20 elevado a 2)


// Operadores de Atribuição. 
let n3 = 20 // Atribuição Básica
// n3 = n3 + 15
n3 += 15
console.log(n3)

// Operadores de Incrmento/Decrmento
let contador = 10;
console.log('Incremento (pós):', contador++); // Saída: 10 (primeiro usa o valor, depois incrementa)
console.log('Valor após incremento:', contador); // Saída: 11

let decrescer = 10;
console.log('Decremento (pré):', --decrescer); // Saída: 9 (primeiro decrementa, depois usa o valor)
console.log('Valor após decremento:', decrescer); // Saída: 9

